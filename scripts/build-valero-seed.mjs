function buildLine(player, odds, market) {
  return {
    player,
    market,
    odds,
    impliedProb: Math.round(americanToImplied(odds) * 10000) / 10000,
    source: "draftkings-live-capture",
    source_label: `DraftKings (live browser capture, ${now.slice(0, 10)})`,
    captured_at: now,
    tournament: SLUG,
    book: "DraftKings",
  };
}

const snapshot = {
  tournament: SLUG,
  generatedAt: now,
  source: "draftkings-live-capture",
  source_label: `DraftKings (live browser capture, ${now.slice(0, 10)})`,
  limitation: null,
  _capture_note: `Lines captured LIVE from DraftKings public sportsbook outrights for ${NAME} (Apr 3-6, TPC San Antonio). Browser-captured via Playwright on ${now.slice(0, 10)}. These are real DK market lines, NOT provisional/synthetic.`,
  top5: top5top10data.map(([player, top5Odds]) => buildLine(player, top5Odds, "top5")),
  top10: top5top10data.map(([player, _t5, top10Odds]) => buildLine(player, top10Odds, "top10")),
  top20: top20data.map(([player, odds]) => buildLine(player, odds, "top20")),
};

