create or replace view public.goose_training_examples_v1 as
select
  agql.candidate_id,
  agql.sport,
  agql.league,
  agql.season,
  agql.event_date,
  agql.team_name,
  agql.opponent_name,
  agql.team_role,
  agql.market_type,
  agql.market_family,
  agql.market_scope,
  agql.segment_key,
  agql.side,
  agql.line,
  agql.odds,
  agql.is_favorite,
  agql.is_underdog,
  agql.is_back_to_back,
  agql.is_prime_time,
  agql.is_divisional_game,
  agql.team_win_pct_pre_game,
  agql.opponent_win_pct_pre_game,
  agql.team_above_500_pre_game,
  agql.opponent_above_500_pre_game,
  agql.previous_moneyline_result,
  agql.previous_over_result,
  agql.previous_under_result,
  agql.result,
  agql.profit_units,
  agql.profit_dollars_10,
  case when agql.profit_units > 0 then 1 else 0 end as profitable_label,
  agql.trends_build_version
from public.ask_goose_query_layer_v1 agql
where agql.graded = true;
